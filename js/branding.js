function toggleBillingCycle() {
  const isAnnual = document.getElementById('billingToggle')?.classList.contains('on');
  PRICING_PLANS.forEach(plan => {
    const priceEl = document.getElementById(`price-${plan.id}`);
    const noteEl = document.getElementById(`priceNote-${plan.id}`);
    if (!priceEl || !plan.price_monthly) return;
    if (isAnnual) {
      const annualMonthly = Math.round(plan.price_annual / 12);
      priceEl.innerHTML = `$${annualMonthly}<span style="font-size:14px;font-weight:400;color:var(--text-muted);">/mo</span>`;
      if (noteEl) noteEl.textContent = `Billed annually · $${plan.price_annual}/yr`;
    } else {
      priceEl.innerHTML = `$${plan.price_monthly}<span style="font-size:14px;font-weight:400;color:var(--text-muted);">/mo</span>`;
      if (noteEl) noteEl.textContent = `Billed monthly · $${plan.price_annual}/yr if annual`;
    }
  });
}

