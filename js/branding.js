function updateBrandingPreview() {
  const name    = document.getElementById('br_name')?.value || BRANDING_STORE.platform_name;
  const tagline = document.getElementById('br_tagline')?.value || BRANDING_STORE.platform_tagline;
  const primary = document.getElementById('br_primary')?.value || BRANDING_STORE.primary_color;
  const accent  = document.getElementById('br_accent')?.value || BRANDING_STORE.accent_color;
  const el = id => document.getElementById(id);
  if (el('prevPlatformName')) el('prevPlatformName').textContent = name;
  if (el('prevTagline')) el('prevTagline').textContent = tagline;
  if (el('prevLoginBtn')) el('prevLoginBtn').style.background = primary;
  if (el('prevLogoIcon')) el('prevLogoIcon').style.background = `linear-gradient(135deg,${primary},${accent})`;
}

function applyTheme(primary, accent, sidebar) {
  document.getElementById('br_primary').value = primary;
  document.getElementById('br_accent').value = accent;
  document.getElementById('br_sidebar').value = sidebar;
  updateBrandingPreview();
  showToast('Theme applied — click Save to confirm','info');
}

function saveBranding() {
  BRANDING_STORE.platform_name   = document.getElementById('br_name')?.value.trim() || BRANDING_STORE.platform_name;
  BRANDING_STORE.platform_tagline = document.getElementById('br_tagline')?.value.trim() || BRANDING_STORE.platform_tagline;
  BRANDING_STORE.primary_color   = document.getElementById('br_primary')?.value || BRANDING_STORE.primary_color;
  BRANDING_STORE.accent_color    = document.getElementById('br_accent')?.value || BRANDING_STORE.accent_color;
  BRANDING_STORE.sidebar_color   = document.getElementById('br_sidebar')?.value || BRANDING_STORE.sidebar_color;
  BRANDING_STORE.support_email   = document.getElementById('br_email')?.value.trim() || BRANDING_STORE.support_email;
  BRANDING_STORE.custom_domain   = document.getElementById('br_domain')?.value.trim() || '';
  BRANDING_STORE.report_header   = document.getElementById('br_rpt_header')?.value.trim() || BRANDING_STORE.report_header;
  BRANDING_STORE.report_footer   = document.getElementById('br_rpt_footer')?.value.trim() || BRANDING_STORE.report_footer;
  // Apply to sidebar logo name
  const logoName = document.querySelector('.logo-name');
  if (logoName) logoName.textContent = BRANDING_STORE.platform_name;
  document.title = `${BRANDING_STORE.platform_name} — Threat Intelligence Platform`;
  document.documentElement.style.setProperty('--accent-blue', BRANDING_STORE.primary_color);
  document.documentElement.style.setProperty('--accent-cyan', BRANDING_STORE.accent_color);
  showToast(`✅ Branding saved — "${BRANDING_STORE.platform_name}" applied!`, 'success');
}

function resetBranding() {
  Object.assign(BRANDING_STORE, {
    platform_name:'ThreatPilot AI', platform_tagline:'AI-Agentic Threat Intelligence Platform',
    primary_color:'#3b82f6', accent_color:'#22d3ee', sidebar_color:'#0d1421',
    logo_icon:'fa-shield-alt', report_header:'ThreatPilot AI — Confidential Threat Intelligence Report',
    report_footer:'Generated by ThreatPilot AI · CONFIDENTIAL — DO NOT DISTRIBUTE',
  });
  document.documentElement.style.removeProperty('--accent-blue');
  document.documentElement.style.removeProperty('--accent-cyan');
  const logoName = document.querySelector('.logo-name');
  if (logoName) logoName.textContent = 'ThreatPilot AI';
  document.title = 'ThreatPilot AI — AI-Agentic Threat Intelligence Platform';
  renderBranding();
  showToast('Branding reset to ThreatPilot AI defaults','info');
}

